# Definindo os Pokémons
class Pokemon:
    def __init__(self, nome, tipo, attack, def_, vida, ascii_art):
        if attack < 0 or def_ < 0 or vida < 0:
            raise ValueError("Os valores de ataque, defesa e vida devem ser positivos.")
        self.nome = nome
        self.tipo = tipo
        self.attack = attack
        self.def_ = def_
        self.vida = vida
        self.ascii_art = ascii_art

    def receber_dano(self, dano):
        """Reduz a vida do Pokémon, garantindo que não fique negativa."""
        self.vida = max(self.vida - dano, 0)
        return self.vida

    def restaurar_vida(self):
        """Restaura a vida do Pokémon ao valor inicial."""
        self.vida = 100

    def exibir(self):
        """Exibe as informações do Pokémon de forma visual."""
        barra_vida = "█" * (self.vida // 10) + "-" * (10 - (self.vida // 10))
        print(f"Nome: {self.nome}")
        print(f"Tipo: {self.tipo}")
        print(f"Vida: {self.vida} [{barra_vida}]")
        print(f"Ataque: {self.attack}")
        print(f"Defesa: {self.def_}")
        print(self.ascii_art)
        print("-" * 40)

    def __str__(self):
        """Retorna uma representação textual do Pokémon."""
        return f"{self.nome} (Tipo: {self.tipo}, Vida: {self.vida}, ATK: {self.attack}, DEF: {self.def_})"


